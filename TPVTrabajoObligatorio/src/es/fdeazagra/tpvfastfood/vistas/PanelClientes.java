        ServiceCliente accion = new ServiceCliente(this, frame);
        
        try {
            accion.saveCliente();
        } catch (SQLException ex) {
            Logger.getLogger(PanelClientes.class.getName())
                    .log(Level.SEVERE, null, ex);
        }
