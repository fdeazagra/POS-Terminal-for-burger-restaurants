    /**
     * Método que acciona el botón para buscar un cliente.
     */
    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        
        ServiceCliente service = new ServiceCliente(this, frame);
        AccionesCliente accion = new AccionesCliente(frame, this);
        
        try {
            service.getCliente();
            accion.clienteAPantalla();
            
        } catch (SQLException ex) {
            Logger.getLogger(PanelClientes.class.getName())
                    .log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscarClienteActionPerformed

